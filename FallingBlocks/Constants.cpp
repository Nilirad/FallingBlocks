#include "Constants.h"
#include <Windows.h>

/////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////  STATIC DATA  /////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////

sf::Font Constants::font = sf::Font();



/////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////  METHODS  /////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////

void Constants::init() {
	font = sf::Font();
	TCHAR tFontPath[MAX_PATH];
	GetCurrentDirectory(MAX_PATH, tFontPath);
	wcscat_s(tFontPath, L"\\res\\font.ttf");
	char fontPath[MAX_PATH];
	wcstombs(fontPath, tFontPath, MAX_PATH);

	if (!font.loadFromFile(fontPath)) {
		throw;
	}
}
